const mongoose = require('mongoose');

const { Schema } = mongoose;
const noticeSchema = new Schema({
    notice_type: {
        type: String,
        required: true,
    },
    subject: {
        type: String,
        required: true,
    },
    content: {
        type: String,
        required: true,
    },
    createdAt: {
        type: Date,
        default: Date.now,
    },
    updatedAt: {
        type: Date,
        default: Date.now,
    },
});

module.exports = mongoose.model('Notice', noticeSchema); // 공지사항 DB export